"""Визначити кількість днів в році, який вводить користувач.
У високосному році — 366 днів, тоді як в звичайному їх 365.
"""
year = int(input('Введіть будь-який рік: '))

# Високосний рік кратний 4, але при цьому не кратний 100, або кратний 400
if (year % 4 == 0 and year % 100 != 0) or year % 400 == 0:
    print('Цей рік високосний.')
else:
    print('Цей рік не високосний.')
