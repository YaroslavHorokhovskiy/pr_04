"""Трикутник існує тільки тоді, коли сума будь-яких двох його сторін більше
третьої. Дано: a, b, c — сторони передбачуваного трикутника. Напишіть програму,
яка вкаже, чи існує такий трикутник чи ні.
"""
a = int(input('Введіть сторону a трикутника: '))
b = int(input('Введіть сторону b трикутника: '))
c = int(input('Введіть сторону c трикутника: '))

if (a + b > c) and (a + c > b) and (b + c > a):
    print('Такий трукутник існує.')
else:
    print('Такий трукутник не існує.')
